fun main(args: Array<String>) {
    /*
    * Conditional Operators
    * ==
    * <
    * >
    * !=
    * <=
    * >=
    * */

    var fNum = 6
    var lNum = 9

    println(fNum < lNum) //return boolean which mean true or false
    println(fNum == lNum)
    println(fNum > lNum)
    println(fNum != lNum)
}